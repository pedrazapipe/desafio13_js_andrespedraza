/* Inicio animaciones Jquery */

/* Mostras show */

$("main__somos").ready(function(){
    $(".suculentas__show").click(function(){
        $(".suculenta__text").show(400);

    });

    $(".carnivora__show").click(function(){
        $(".carnivora__text").show(400);

    });

/* Esconder hide */

    $(".suculentas__hide").click(function(){
        $(".suculenta__text").hide(400);

    });

    $(".carnivora__hide").click(function(){
        $(".carnivora__text").hide(400);

    });

});




/* Final animaciones Jquery */